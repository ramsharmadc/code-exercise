# what are different types and how to get it

def get_type_of(x):
    print(type(x))


get_type_of(print)
get_type_of(type)
get_type_of(str)
get_type_of(ord)
get_type_of(chr)
get_type_of("Hello")
get_type_of(2.334)
get_type_of('199')
get_type_of(int('199'))
get_type_of(True)
get_type_of(5>10) # the expression generated a boolean value of type 'bool'

