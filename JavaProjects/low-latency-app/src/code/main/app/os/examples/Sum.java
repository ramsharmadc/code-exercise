package code.main.app.os.examples;

public class Sum {
    private long sum;

    public Sum(Integer value) {
        this();
        this.sum = value;
    }

    public Sum() {
        super();
    }

    public long getSum() {
        return sum;
    }

    public void setSum(int sum) {
        this.sum = sum;
    }
}
